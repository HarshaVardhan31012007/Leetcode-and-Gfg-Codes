class Solution {
  public:
    bool isCBT(Node* root) {
        queue<Node*>q;
        q.push(root);
        bool nullFound=0;
        while(!q.empty()){
            Node *front=q.front();
            q.pop();
            if(front==NULL){
                nullFound=1;
            }
            else{
                if(nullFound==true)
                return false;
                else{
                    q.push(front->left);
                    q.push(front->right);
                }
            }
        }
        return true;
    }
    bool property(Node *root){
        if(!root||!root->left&&!root->right) return true;
        bool lans=property(root->left);
        bool rans=property(root->right);
        bool option1=true;
        if(root->left&&root->data<root->left->data)
        option1=false;
        bool option2=true;
        if(root->right&&root->data<root->right->data)
        option2=false;
        bool currans=option1&&option2;
        return currans&&lans&&rans;
    }
    bool isHeap(struct Node* tree) {
        return isCBT(tree)&&property(tree);
    }
};

